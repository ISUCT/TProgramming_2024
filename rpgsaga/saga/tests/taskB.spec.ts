import { taskB } from "../src";

describe('test taskB', () => {
    it('should return [-2.5889837582999116, -3.5670236341584967, -4.609305176081285, -5.874074021040143, -7.553731110582941]', () => {
        const result = [-2.5889837582999116, -3.5670236341584967, -4.609305176081285, -5.874074021040143, -7.553731110582941];
        expect(taskB([1.1, 1.2, 1.3, 1.4, 1.5])).toEqual(result);
    })
    it('should return []', () => {
        const result = [];
        expect(taskB([])).toEqual(result);
    })
    it('should return [5.808540922067882, 5.451774615260347, 5.151132589075272, 4.894697076545544, 4.673712880620777]', () => {
        const result = [5.808540922067882, 5.451774615260347, 5.151132589075272, 4.894697076545544, 4.673712880620777];
        expect(taskB([3.1, 3.2, 3.3, 3.4, 3.5])).toEqual(result);
    })
});